package bvworks.apimonitor.bean;

public class CallCountBean {
	private String date;
	private int count;
	
	public CallCountBean() {}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}
	
	
}
